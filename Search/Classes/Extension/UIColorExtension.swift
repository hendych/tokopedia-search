import UIKit

extension UIColor {
    open class var lightGreen: UIColor {
        return UIColor(red: 98.0 / 255.0, green: 174.0 / 255.0, blue: 86.0 / 255.0, alpha: 1)
    }
    open class var mediumGray: UIColor {
        return UIColor(white: 0.64, alpha: 1)
    }
    open class var thinGray: UIColor {
        return UIColor(white: 0.9, alpha: 1)
    }
    open class var brokenWhite: UIColor {
        return UIColor(white: 248.0 / 255.0, alpha: 1)
    }
}
